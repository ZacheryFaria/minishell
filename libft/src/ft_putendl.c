#include <unistd.h>

void	ft_putendl(const char *s)
{
	if (s == 0)
		return ;
	while (*s)
	{
		if (write(1, s, 1) < 0)
			return ;
		s++;
	}
	if (write(1, "\n", 1) < 0)
		return ;
}
