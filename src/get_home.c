#include <libft.h>

char		**g_env;

char		*get_home(void)
{
	int i;

	i = 0;
	while (g_env[i])
	{
		if (ft_strstr(g_env[i], "HOME"))
			return (g_env[i] + 5);
		i++;
	}
	return (0);
}
